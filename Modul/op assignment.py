# operasi assignment adalah op yg dapat dilakukan dengan penyingkatan
# operasi ditambah dengan assignment

a = 5 # ini adalah assignment
print("nilai a =",a)

a += 5 # ini adalah a = a + 5
print("nilai a += 5, nilai a menjadi",a)

a -= 4 # ini adalah a = a - 4
print("nilai a -= 4, nilai a menjadi",a)

a *= 2 # ini adalah a = a * 2
print("nilai a *= 2, nilai a menjadi",a)

a /= 3 # ini adalah a = a / 3
print("nilai a /= 3, nilai a menjadi",a)

#coba yang modulus
b = 100
print("nilai b =",b)

b %= 3 # ini adalah b = b %= 3
print("nilai b %= 3, maka nilai b menjadi",b)

b //= 3 # ini adalah b = b //= 3
print("nilai b //= 3, maka nilai b menjadi",b)

# coba eksponen **
a = 5
print("nilai a=",a)

a **= 3
print("nilai a **= 3, nilai a menjadi",a)