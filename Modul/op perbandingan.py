# operasi komparasi atau perbandingan

# setiap hasil dari operasi komparasi adalah boolean yaitu true atau false

# >,<,>=,<=,==,!=, is, is not

a = 4
b = 2

# lebih besar dari >
print("===lebih besar dari (>)")
hasil = a > 3 #apakah a lebih besar dari 3??
print(a,">",3,"=",hasil)

# kurang dari <
print("===kurang besar dari (<)")
hasil = a < 1
print(a,"<",1,"=",hasil)
hasil = a < 10
print(a,"<",10,"=",hasil)
hasil = a < 4
print(a,"<",4,"=",hasil)

# lebih dari atau sama dengan >=
print("===lebih dari atau sama dengan (>=)")
hasil = a >= 3
print(a,">=",3,"=",hasil)
hasil = a >= 10
print(a,">=",10,"=",hasil)
hasil = a >= 4
print(a,">=",4,"=",hasil)

# kurang dari atau sama dengan <=
print("===kurang dari atau sama dengan (<=)")
hasil = a <= 3
print(a,"<=",3,"=",hasil)
hasil = a <= 10
print(a,"<=",10,"=",hasil)
hasil = a <= 4
print(a,"<=",4,"=",hasil)

# sama dengan ==
print("===sama dengan (==)")
hasil = a == 3
print(a,"==",3,"=",hasil)
hasil = a == 10
print(a,"==",10,"=",hasil)
hasil = a == 4
print(a,"==",4,"=",hasil)

# tidak sama dengan !=
print("!==tidak sama dengan (!=)")
hasil = a != 3
print(a,"!=",3,"=",hasil)
hasil = a != 10
print(a,"!=",10,"=",hasil)
hasil = a != 4
print(a,"!=",4,"=",hasil)

